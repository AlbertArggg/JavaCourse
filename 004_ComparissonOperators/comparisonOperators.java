public class comparisonOperators 
{
	public static void main(String[] args)
	{
		if(7 == 3) {System.out.println("7 == 3 -> true");}		// equality comparison operator
		else {System.out.println("7 == 3 -> false");}			// if 7 equal 3 prints true. otherwise, prints false
		
		if(7 != 3) {System.out.println("7 != 3 -> true");}		// inequality comparison operator
		else {System.out.println("7 != 3 -> false");}			// if 7 is not equal to 3 prints true. otherwise, prints false
		
		if(7 > 3) {System.out.println("7 > 3  -> true");}		// greater than comparison operator
		else {System.out.println("7 > 3  -> false");}			// if 7 greater than 3 prints true. otherwise, prints false
		
		if(7 < 3) {System.out.println("7 < 3  -> true");}		// less than comparison operator
		else {System.out.println("7 < 3  -> false");}			// if 7 less than 3 prints true. otherwise, prints false
		
		if(7 >= 3) {System.out.println("7 >= 3 -> true");}		// less than or equal to comparison operator
		else {System.out.println("7 >= 3 -> false");}			// if 7 greater than or equal to 3 prints true. otherwise, prints false
		
		if(7 <= 3) {System.out.println("7 <= 3 -> true");}		// less than or equal to comparison operator
		else {System.out.println("7 <= 3 -> false");}			// if 7 less than or equal to 3 prints true. otherwise, prints false
	}
}
